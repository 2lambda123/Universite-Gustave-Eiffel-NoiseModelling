/*
 * A segment built from the combination of 2 vertices index.
 */
public class IntSegment {
	private int a=0;
	private int b=0;
	public IntSegment(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}

}
